

-- Create Users Table
CREATE TYPE user_role AS ENUM ('Ticket Manager', 'Football Fan');

CREATE TABLE Users (
  user_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  full_name VARCHAR(100) NOT NULL,
  email VARCHAR(100) UNIQUE NOT NULL,
  role user_role NOT NULL,
  phone_number VARCHAR(20)
);


-- Create Matches Table
CREATE TYPE match_status_type AS ENUM ('Available', 'Selling Fast', 'Sold Out', 'Postponed');

CREATE TABLE Matches (
  match_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fixture VARCHAR(150) NOT NULL,
  tournament_category VARCHAR(100) NOT NULL,
  base_ticket_price DECIMAL(10, 2) NOT NULL,
  match_status match_status_type NOT NULL
);


-- Create Bookings Table (Junction Table)
CREATE TYPE payment_status_type AS ENUM ('Pending', 'Confirmed', 'Cancelled', 'Refunded');

CREATE TABLE Bookings (
  booking_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INT NOT NULL,
  match_id INT NOT NULL,
  seat_number VARCHAR(10),
  payment_status payment_status_type,
  total_cost DECIMAL(10,2) NOT NULL CHECK(total_cost >= 0),
  
  -- Referential Constraints
  
  CONSTRAINT fk_bookings_user FOREIGN KEY (user_id) REFERENCES Users (user_id) ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_bookings_match FOREIGN KEY (match_id) REFERENCES Matches (match_id) ON DELETE RESTRICT ON UPDATE CASCADE
);

-- Query one

SELECT
    match_id,
    fixture,
    base_ticket_price
FROM matches
WHERE tournament_category = 'Champions League'
AND match_status = 'Available';


